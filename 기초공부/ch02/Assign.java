package ch02;

public class Assign {
	public static void main(String[] args) {
		//주석처리: 마우스 드래그 후 Ctrl+/하면 드래그한 코드 주석처리됨
		//같은 자료형일 경우 , 사용 가능
		int a=10, b=20;
		a += 5;
		System.out.println(a);
		a -= 5;
		System.out.println(a);
		b *= 5;
		System.out.println(b);
		b /= 2;
		System.out.println(b);
	}
}
